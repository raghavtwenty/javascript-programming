// Number
let integerNumber = 42; // Integer
console.log(integerNumber);      

let floatNumber = 3.14; // Floating-point number
console.log(floatNumber);        


// String
let singleQuoteString = 'Hello, World!'; // Using single quotes
console.log(singleQuoteString);  

let doubleQuoteString = "Hello, World!"; // Using double quotes
console.log(doubleQuoteString);  


// Boolean
let isJavaScriptFun = true; // Boolean true
console.log(isJavaScriptFun);   

let isJavaScriptHard = false; // Boolean false
console.log(isJavaScriptHard);   


// Null
let emptyValue = null; // Null
console.log(emptyValue);   


// Undefined
let notAssigned; // Undefined
console.log(notAssigned); 


// BigInt
let bigIntNumber = 9007199254740991n; // BigInt
console.log(bigIntNumber);      


// Symbol
let uniqueId = Symbol('id'); // Unique identifier
console.log(uniqueId);   